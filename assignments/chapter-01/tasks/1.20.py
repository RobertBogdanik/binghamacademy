import turtle

turtle.penup()
turtle.goto(100, 50)
turtle.pendown()
turtle.goto(100, -50)
turtle.goto(-100, -50)
turtle.goto(-100, 50)
turtle.goto(100, 50)


turtle.goto(150, 75)
turtle.goto(150, -25)
turtle.goto(-50, -25)
turtle.goto(-50, 75)
turtle.goto(150, 75)

turtle.goto(150, -25)
turtle.goto(100, -50)


turtle.goto(-100, -50)
turtle.goto(-50, -25)

turtle.goto(-50, 75)
turtle.goto(-100, 50)

turtle.mainloop()
